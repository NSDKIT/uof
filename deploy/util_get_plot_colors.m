%% =========================================================
%  util_get_plot_colors.m  ―  グラフ描画用カラーパレットの定義
% =========================================================
%
%  【役割】
%    グラフ描画に使用するカラーパレット（color, mycolor_13）を
%    ワークスペースに定義するスクリプト。
%    関数ではなくスクリプトのため、run または呼び出し元で実行すると
%    変数がそのままワークスペースに展開される。
%
%  【実行方法】
%    util_get_plot_colors  % コマンドウィンドウまたは他スクリプトから呼び出す
%
%  【出力変数（ワークスペースに展開）】
%    color       : 14色のRGB配列（14×3, 0〜1に正規化済み）
%    mycolor_13  : 13色のカラーマップ（PV・火力・水力向けの配色）
%
%  【使用するスクリプト】
%    step5_calc_sigma_basic.m  ― σ計算グラフの描画
%
%  【注意事項】
%    - このファイルはスクリプト（functionではない）のため、
%      呼び出し元のワークスペースに color, mycolor_13 が直接定義される。
%    - color は 14行×3列 の配列（各行が1色のRGB値，0〜1に正規化）。
%    - mycolor_13 は PV・火力・水力の発電種別を想定した13色配色。
% =========================================================

%% 14色のRGBカラーパレット定義
color = [
% red（赤）
255 0 0
% pink（ピンク）
205 92 92
% orenge（オレンジ）
230 121 40
% oudo（黄土色）
186 132 72
% yellow（黄色）
237 185 24
% black-green（黒緑）
84 135 34
% green（緑）
0 136 51
% black-blue（黒青）
17 17 136
% blue-purple（青紫）
0 0 255
% indigo（藍色）
19 74 99
% purple（紫）
167 87 168
% red-purple（赤紫）
136 34 85
% gray（灰色）
204 204 204
% black（黒に近い白）
225 225 225
];
color = color ./ 255;  % 0〜255 → 0〜1 に正規化

%% 13色カラーマップ（PV・火力・水力向け）
oil_color=flipud(turbo(10));
coal_color=gray(10);
water_color=cool(10);
mycolor_13 = [oil_color(4,:);
    oil_color(1:3,:);
    [170,134,66]/255;
    coal_color(3:8,:);
    [136,34,85]/255;
    water_color(1,:)];

